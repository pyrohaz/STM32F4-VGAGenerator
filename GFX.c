uint8_t WritePix(int16_t X, int16_t Y, PixT V){
	if(X>P_XPix-1 || X<0 || Y>P_YPix-1 || Y<0) return 1;

	uint32_t Index;
	uint32_t CPix;

	/*Index = X+((Y>>4)*P_XPix);
	CPix = P_GBuf[Index];

	if(V == PixNorm) CPix |= 1<<(Y%16);
	else CPix &= ~(1<<(Y%16));

	P_GBuf[Index] = CPix;*/

	//Index = (X>>4)+Y*(P_XPix>>4);
	Index = (X/16)+Y*(P_XPix/16);
	CPix = P_GBuf[Index];

	if(V == PixNorm) CPix |= 1<<(15-(X&15));
	else if(V == PixInv) CPix &= ~(1<<(15-(X&15)));

	P_GBuf[Index] = CPix;

	return 0;
}

uint8_t SetPix(uint8_t X, uint8_t Y){
	return WritePix(X, Y, PixNorm);
}

uint8_t ClrPix(uint8_t X, uint8_t Y){
	return WritePix(X, Y, PixInv);
}

uint8_t Circle(uint8_t XS, uint8_t YS, uint8_t R, PixT V){
	if((XS+R)>P_XPix || (YS+R)>P_YPix) return 1;

	int X, Y, D, X2M1;
	Y = R;
	D = -R;
	X2M1 = -1;

	for(X = 0; X<R/1.414213562f; X++){
		X2M1+=2;
		D+=X2M1;
		if(D>=0){
			Y--;
			D-= (Y<<1);
		}
		WritePix(XS+X, YS+Y, V);
		WritePix(XS-X, YS+Y, V);
		WritePix(XS+X, YS-Y, V);
		WritePix(XS-Y, YS-Y, V);
		WritePix(XS+Y, YS+X, V);
		WritePix(XS-Y, YS+X, V);
		WritePix(XS+Y, YS-X, V);
		WritePix(XS-Y, YS-X, V);
	}
	return 0;
}

